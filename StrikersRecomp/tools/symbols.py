#!/usr/bin/env python3
"""Generate the SDK symbol intercept table for the Aurora HLE layer.

StrikersRecomp runs the game's recompiled machine code natively, but the
GameCube SDK functions that touch hardware (GX/VI/PAD/SI/AI/DSP/DVD/CARD/OS)
cannot run without real hardware. Instead we intercept them at their *guest
addresses* and redirect into Aurora's source-level reimplementation.

This tool builds the address -> SDK-function-name table that the HLE dispatcher
(`runtime/host/sdk_map.c`) consumes. The intercept candidate set is the
intersection of:

  * the recompiled binary's symbol map (from a public `smstrikers-decomp`
    checkout -- CC0, no copyrighted game data), and
  * the SDK functions Aurora actually implements (declared in its
    `include/dolphin/*.h` headers).

Intersecting guarantees every emitted address has a real Aurora implementation
behind it. The game ships no symbols itself; you supply the decomp's
`config/G4QE01/symbols.txt`, exactly as you supply the ISO to `generate.py`.

Usage:
    python3 tools/symbols.py [--decomp ../smstrikers-decomp] [--aurora ../DolRuntime/graphics/aurora]
                             [--out generated/sdk_symbols.inc]
"""
import argparse
import os
import re
import sys

# Identifier immediately preceding a '(' in a header: a function declaration or
# call. Restricted to leading uppercase so we skip lowercase helper macros.
HEADER_FUNC_RE = re.compile(r"\b([A-Z][A-Za-z0-9_]+)\s*\(")

# "Name = .section:0xADDR; // type:function size:0xNN scope:... align:..."
SYMBOL_RE = re.compile(
    r"^(\S+)\s*=\s*\.\w+:0x([0-9A-Fa-f]+);.*type:function", )


def aurora_sdk_names(aurora_dir):
    """Names that appear as functions in Aurora's dolphin SDK headers."""
    names = set()
    hdr_dir = os.path.join(aurora_dir, "include", "dolphin")
    if not os.path.isdir(hdr_dir):
        sys.exit(f"error: Aurora dolphin headers not found at {hdr_dir}")
    for root, _dirs, files in os.walk(hdr_dir):
        for fn in files:
            if not fn.endswith((".h", ".hpp")):
                continue
            with open(os.path.join(root, fn), errors="replace") as f:
                for m in HEADER_FUNC_RE.finditer(f.read()):
                    names.add(m.group(1))
    return names


def decomp_functions(decomp_dir):
    """name -> address for every function in the decomp symbol map."""
    path = os.path.join(decomp_dir, "config", "G4QE01", "symbols.txt")
    if not os.path.isfile(path):
        sys.exit(f"error: decomp symbol map not found at {path}\n"
                 "       point --decomp at a smstrikers-decomp checkout "
                 "(https://github.com/yannicksuter/smstrikers-decomp)")
    funcs = {}
    with open(path, errors="replace") as f:
        for line in f:
            m = SYMBOL_RE.match(line.strip())
            if m:
                funcs[m.group(1)] = int(m.group(2), 16)
    return funcs


def main():
    here = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--decomp", default=os.path.join(here, "..", "smstrikers-decomp"))
    ap.add_argument("--aurora",
                    default=os.path.join(here, "..", "DolRuntime", "graphics", "aurora"))
    ap.add_argument("--out", default=os.path.join(here, "generated", "sdk_symbols.inc"))
    args = ap.parse_args()

    sdk = aurora_sdk_names(args.aurora)
    funcs = decomp_functions(args.decomp)

    matched = sorted(
        ((addr, name) for name, addr in funcs.items() if name in sdk),
        key=lambda t: t[0],
    )

    os.makedirs(os.path.dirname(args.out), exist_ok=True)
    with open(args.out, "w") as out:
        out.write("// Auto-generated by tools/symbols.py -- do not edit.\n")
        out.write("// address -> Aurora-implemented SDK function, intersection of\n")
        out.write("// the smstrikers-decomp symbol map and Aurora's dolphin headers.\n")
        out.write(f"// {len(matched)} symbols out of {len(funcs)} decomp functions.\n\n")
        for addr, name in matched:
            out.write(f'    {{ 0x{addr:08X}u, "{name}" }},\n')

    print(f"symbols.py: {len(funcs)} decomp functions, "
          f"{len(sdk)} Aurora SDK names, {len(matched)} intercept candidates")
    print(f"symbols.py: wrote {args.out}")


if __name__ == "__main__":
    main()
